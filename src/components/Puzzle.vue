<script>
import { gsap } from 'gsap'
import { Draggable } from 'gsap/Draggable'

export default {
  name: 'PuzzleMain',
  mounted() {
    this.initializePuzzle()
  },
  methods: {
    initializePuzzle() {
      // Register Draggable plugin
      gsap.registerPlugin(Draggable)

      // Throw pieces animation
      gsap.to('.piece', {
        duration: 2.1,
        x: () => this.random(),
        y: () => this.random(),
        delay: 0.5,
        ease: 'elastic.inOut',
        stagger: 0.03,
      })

      // Make pieces draggable
      const snapSize = 25
      Draggable.create('.piece', {
        throwProps: true,
        snap: {
          x: function (endValue) {
            return Math.round(endValue / snapSize) * snapSize
          },
          y: function (endValue) {
            return Math.round(endValue / snapSize) * snapSize
          },
        },
      })

      // Balloon animation
      gsap.to('#balloon-innards', {
        duration: 4,
        attr: {
          transform: 'matrix(1, 0, 0, 1, 0, 60)',
        },
        repeat: -1,
        yoyo: true,
        ease: 'power1.inOut',
      })
    },
    random() {
      return (Math.random() * 4 - 2) * 60
    },
  },
}
</script>

